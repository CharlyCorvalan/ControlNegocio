public void cargarCombo(){
    proArray=proData.listarProductos();
    ComboCategoria.addItem("Categoria");
    ComboMarca.addItem("Marca");
    for (Producto producto : proArray) {
        ComboCategoria.addItem(producto.getCategoria());
        ComboMarca.addItem(producto.getMarca());
    }
}
private void armarCabecera(){
    modelo.addColumn("codigo");
    modelo.addColumn("Categoria");
    modelo.addColumn("Marca");
    modelo.addColumn("Descripcion");
    TablaProductos.setModel(modelo); 
}
private void cargarDatos(){    
    TextoProducto.setText("");
    String cod=TextoCodigo.getText();
    String marca=TextoMarca.getText();
    String categoria=TextoCategoria.getText();
    String descripcion=TextoDescripcion.getText();
    String precio=TextoPrecio.getText();
    
    
    
    TextoProducto.append("Codigo : "+cod+"\n"+"Categoria: "+categoria+"\n"+" Marca: "+marca+"\n"+"Descripcion :"
            + ""+descripcion+"\n"+"Precio :"+precio);

}


