    private void OpcAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OpcAgregarActionPerformed
        OpcEditar.setSelected(false);
        OpcEliminar.setSelected(false);
        TextoCodigo.setEditable(false);
        BuscarCategoria.setEnabled(false);
        BuscarMarca.setEnabled(false);
        TextoCodigo.setText("");
        int contador=1;        
        proArray=proData.listarProductos();
        if(proArray.isEmpty()){
            TextoCodigo.setText("1");
        }else{
            for (Producto producto : proArray) {
                if(producto.getCodigoProducto()==contador){
                    contador++;
                }else{
                    TextoCodigo.setText(contador+"");
                    break;
                }
            }
        }       
    }//GEN-LAST:event_OpcAgregarActionPerformed

    private void BotonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonAceptarActionPerformed
        String categoria=TextoCategoria.getText();
        String marca=TextoMarca.getText();
        String descripcion=TextoDescripcion.getText();
        String pre=TextoPrecio.getText();
        Producto producto=new Producto();
        if(categoria.isEmpty()||marca.isEmpty()||descripcion.isEmpty()||pre.isEmpty()){
            JOptionPane.showMessageDialog(null, "Todos los campos son requeridos");
        }else{
            if(OpcAgregar.isSelected()){
            try{
                double precio=Double.parseDouble(pre);
                producto.setCategoria(categoria);
                producto.setMarca(marca);
                producto.setDescripcion(descripcion);
                producto.setPrecio(precio);
                proData.cargarProducto(producto);
            }catch(NumberFormatException x){
                JOptionPane.showMessageDialog(null, "Debe colocar solo numeros en el campo de precio");
            }
        }else if(OpcEditar.isSelected()){
            
        }
        }
        
    }//GEN-LAST:event_BotonAceptarActionPerformed

